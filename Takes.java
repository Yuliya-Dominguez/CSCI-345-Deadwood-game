public class Takes {
    private int number;
    private Area area;

    public Takes(int number, Area area) {
        this.number = number;
        this.area = area;
    }

    public void setNum(int number) {
        this.number = number;
    }
  
    public int getNum() {
        return number;
    }

    public Area getArea() {
        return area;
    }

    public void setArea(Area area) {
        this.area = area;
    }
}
